
public class Dot {
	double x;
	double y;
	int acc; 
	
	Dot(double x, double y, int acc){
		this.x = x;
		this.y = y;
		this.acc = acc;
	}
	
	public double getX() {
		return x;
	}
	
	public double getY() {
		return y;
	}
	
	public int getAcc() {
		return acc;
	}
	
	public void setX(double x) {
		this.x = x;
	}
	
	public void setY(double y) {
		this.y = y;
	}
	
}
